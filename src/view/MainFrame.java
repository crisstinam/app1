    public MainFrame() {
        initComponents();
        model = new DefaultListModel<>();
        jList1.setModel(model);
        jList1.addMouseListener(new MouseAdapter() {
            public void mouseClicked (MouseEvent ev){
                if (ev.getClickCount() ==2){
                    Produs produs =jList1.getSelectedValue();
                    MainService.getInstance().stergeProdus(produs.getId());
                    afisareProduse();
                }
            
            }
        });
        
        jButton1.addActionListener(ev ->adaugaProdus());
        jButton2.addActionListener(ev ->sumaProduse());
        jMenuItem1.addActionListener(ev -> logout());
        
        afisareProduse();
        
        setLocationRelativeTo(null);
        setVisible(true);
    }
    
    private void adaugaProdus(){
        String nume = jTextField1.getText();
        double pret = Double.parseDouble(jTextField2.getText());
        Produs p = new Produs(nume, pret);
        MainService.getInstance().adaugaProdus(p);
        afisareProduse();
        JOptionPane.showMessageDialog(null, "A fost adaugat produsul");
    
    }
    private void logout(){
        new LoginFrame();
        dispose();
    
    }
    
    private void afisareProduse(){
        List<Produs> produse = MainService.getInstance().getAllProducts();
        model.clear();
        produse.forEach(model::addElement);
    
    }
    
    private void sumaProduse(){
        double suma = MainService.getInstance().sumaProduse();
        JOptionPane.showMessageDialog(null,"Suma produse este: "+suma);
    }
    
